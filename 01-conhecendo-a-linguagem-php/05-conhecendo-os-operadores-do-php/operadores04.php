<?php

$a = 30;

$b = 55;

//Valor menor
var_dump($a > $b);

echo "<br/>";

//Valor maior
var_dump($a < $b);

echo "<br/>";

//Valor de atribuição
var_dump($a = $b);

echo "<br/>";

//Valor de comparação
var_dump($a == $b);
echo "<br/>";

//valor de igualdade de identidade
var_dump($a === $b);
echo "<br/>";

//Valor diferente
var_dump($a != $b);

?>